package com.xuelei.tools;

public class Node {
    public int v;  // 值
    public Node l; // 左孩子
    public Node r; // 右孩子


    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }

    public Node getL() {
        return l;
    }

    public void setL(Node l) {
        this.l = l;
    }

    public Node getR() {
        return r;
    }

    public void setR(Node r) {
        this.r = r;
    }
}
